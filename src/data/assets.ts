function generateSparkline(base: number, volatility: number, points = 20): number[] {
  const data: number[] = [];
  let current = base;
  for (let i = 0; i < points; i++) {
    current = current + (Math.random() - 0.48) * volatility;
    data.push(parseFloat(current.toFixed(4)));
  }
  return data;
}

